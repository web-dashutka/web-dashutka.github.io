  function langSwitcher(lang) {

    $('.lang').each(function(idx, elem){

      if ($(this)[0].innerHTML !== undefined) {

        if(elem.localName === 'input') {
          $(this)[0].placeholder = arrLang[lang][$(this)[0].attributes[0].value];
        }

        $(this)[0].innerHTML = arrLang[lang][$(this)[0].attributes[0].value];
      }

      
    })
  }


  $('.header__lang-toggle-btn').click(function() {
    $(this).addClass('header__lang-toggle-btn--active');
    $(this).siblings().removeClass('header__lang-toggle-btn--active');
    let lang = $(this).attr('flag');
    langSwitcher(lang);
  })

